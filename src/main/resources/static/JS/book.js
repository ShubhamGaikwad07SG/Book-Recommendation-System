// Populate the state dropdown when the page loads
window.onload = () => {
  const stateSelect = document.getElementById('state');
  for (const state in data) {
    const option = document.createElement('option');
    option.value = state;
    option.textContent = state;
    stateSelect.appendChild(option);
  }
}

// Load districts based on selected state
function loadDistricts() {
  const stateSelect = document.getElementById('state');
  const districtSelect = document.getElementById('district');
  const talukaSelect = document.getElementById('taluka');
  const pincodeInput = document.getElementById('pincode');

  // Clear existing selections
  districtSelect.innerHTML = "<option value=''>Select District</option>";
  talukaSelect.innerHTML = "<option value=''>Select Taluka</option>";
  pincodeInput.value = '';  // Clear pincode

  if (stateSelect.value) {
    const districts = data[stateSelect.value];
    for (const district in districts) {
      const option = document.createElement('option');
      option.value = district;
      option.textContent = district;
      districtSelect.appendChild(option);
    }
  }
}

// Load talukas based on selected district
function loadTalukas() {
  const stateSelect = document.getElementById('state');
  const districtSelect = document.getElementById('district');
  const talukaSelect = document.getElementById('taluka');
  const pincodeInput = document.getElementById('pincode');

  // Clear existing talukas and pincode
  talukaSelect.innerHTML = "<option value=''>Select Taluka</option>";
  pincodeInput.value = ''; // Clear pincode

  if (stateSelect.value && districtSelect.value) {
    const talukas = data[stateSelect.value][districtSelect.value];
    for (const taluka in talukas) {
      const option = document.createElement('option');
      option.value = taluka;
      option.textContent = taluka;
      talukaSelect.appendChild(option);
    }
  }
}

// Set pincode based on selected taluka
function loadPincode() {
  const stateSelect = document.getElementById('state');
  const districtSelect = document.getElementById('district');
  const talukaSelect = document.getElementById('taluka');
  const pincodeInput = document.getElementById('pincode');

  if (stateSelect.value && districtSelect.value && talukaSelect.value) {
    const pincode = data[stateSelect.value][districtSelect.value][talukaSelect.value];
    pincodeInput.value = pincode;  // Set the pincode in the input field
  }
}






